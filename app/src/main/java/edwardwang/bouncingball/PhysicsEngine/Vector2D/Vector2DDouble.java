package edwardwang.bouncingball.PhysicsEngine.Vector2D;

/**
 * Created by edwardwang on 8/4/16.
 */
public class Vector2DDouble {
    double x, y;

    public Vector2DDouble() {
        x = 0;
        y = 0;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public void setX(double x) {
        this.x = x;
    }

    public void setY(double y) {
        this.y = y;
    }
}
