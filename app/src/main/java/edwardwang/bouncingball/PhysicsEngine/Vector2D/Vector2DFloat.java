package edwardwang.bouncingball.PhysicsEngine.Vector2D;

/**
 * Created by edwardwang on 8/5/16.
 */
public class Vector2DFloat {
    float x, y;

    public Vector2DFloat() {
        x = 0f;
        y = 0f;
    }

    public float getX() {
        return x;
    }

    public float getY() {
        return y;
    }

    public void setX(float x) {
        this.x = x;
    }

    public void setY(float y) {
        this.y = y;
    }
}
