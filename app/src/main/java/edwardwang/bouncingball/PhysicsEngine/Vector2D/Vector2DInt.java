package edwardwang.bouncingball.PhysicsEngine.Vector2D;

/**
 * Created by edwardwang on 8/4/16.
 */
public class Vector2DInt {
    int x, y;

    public Vector2DInt() {
        x = 0;
        y = 0;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }
}
