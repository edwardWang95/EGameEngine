package edwardwang.bouncingball.PhysicsEngine.Vector2D;

/**
 * Created by edwardwang on 8/4/16.
 */
public class Vector2DLong {
    long x, y;

    public Vector2DLong() {
        x = 0;
        y = 0;
    }

    public long getX() {
        return x;
    }

    public long getY() {
        return y;
    }

    public void setX(long x) {
        this.x = x;
    }

    public void setY(long y) {
        this.y = y;
    }
}
